import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const projectRoot = process.cwd();
const lockfile = JSON.parse(readFileSync(join(projectRoot, 'package-lock.json'), 'utf8'));
const packages = Object.entries(lockfile.packages)
  .filter(([packagePath, metadata]) => packagePath.startsWith('node_modules/') && metadata.dev !== true)
  .map(([packagePath, metadata]) => {
    const packageDirectory = join(projectRoot, ...packagePath.split('/'));
    const packageJsonPath = join(packageDirectory, 'package.json');
    return { packageDirectory, packageJsonPath, version: metadata.version };
  })
  .filter(({ packageJsonPath }) => existsSync(packageJsonPath));

const licenseCandidates = [
  'LICENSE',
  'LICENSE.md',
  'LICENSE.txt',
  'LICENCE',
  'LICENCE.md',
  'LICENCE.txt',
  'COPYING'
];

const sections = packages
  .map(({ packageDirectory, packageJsonPath, version }) => {
    const packageJson = JSON.parse(readFileSync(packageJsonPath, 'utf8'));
    return { name: packageJson.name, packageDirectory, packageJson, version };
  })
  .sort((left, right) => left.name.localeCompare(right.name))
  .map(({ name, packageDirectory, packageJson, version }) => {
    const licensePath = licenseCandidates
      .map(candidate => join(packageDirectory, candidate))
      .find(existsSync);

    if (!licensePath) {
      throw new Error(`No license file found for ${name}@${version}`);
    }

    const repository = typeof packageJson.repository === 'string'
      ? packageJson.repository
      : packageJson.repository?.url || packageJson.homepage || '';
    const licenseText = readFileSync(licensePath, 'utf8').trim();

    return [
      `## ${name}@${version}`,
      '',
      `License: ${packageJson.license || 'See included text'}`,
      repository ? `Source: ${repository}` : null,
      '',
      '```text',
      licenseText,
      '```'
    ].filter(line => line !== null).join('\n');
  });

const notice = [
  '# Third-Party Notices',
  '',
  'This file covers packages in the installed production dependency tree.',
  'It is generated by `npm run notices`; do not edit it manually.',
  '',
  ...sections,
  ''
].join('\n');

writeFileSync(join(projectRoot, 'THIRD_PARTY_NOTICES.md'), notice, 'utf8');
writeFileSync(join(projectRoot, 'public', 'third-party-notices.txt'), notice, 'utf8');

console.log(`Generated notices for ${packages.length} production packages.`);
